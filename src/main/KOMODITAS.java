    public void TabelKomoditas()
    {
        try {
            pst = con.prepareStatement("SELECT * FROM KOMODITAS");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData RSM = rs.getMetaData();
            int c;
            c = RSM.getColumnCount();
            
            KOM = (DefaultTableModel)TabelKomoditas.getModel();
            KOM.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++)
                {
                    v2.add(rs.getString("Kode_Komoditas"));
                    v2.add(rs.getString("Sektor_Komoditas"));
                    v2.add(rs.getString("Nilai_Ekspor"));
                    v2.add(rs.getString("Pangsa_Total"));
                    
                }
                KOM.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(KOMODITAS.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
