    private void INSERT_KOMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_INSERT_KOMActionPerformed
        
        String Kode_Kom = Kode_Komoditas.getText();
        String Nil_Eks = Nilai_Ekspor.getText();
        String Sek_Kom = Sektor_Komoditas.getSelectedItem().toString();
        String Pang_Tot = Pangsa_Total.getText();
        
        
        try {
            pst = con.prepareStatement("INSERT INTO KOMODITAS VALUES (?,?,?,?)");
            pst.setString(1, Kode_Kom);
            pst.setString(2, Sek_Kom);
            pst.setString(3, Nil_Eks);
            pst.setString(4, Pang_Tot);
            
           
            int k = pst.executeUpdate();
            
            if (k==1)
            {
                JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan");
                
                Kode_Komoditas.setText("");
                Sektor_Komoditas.setSelectedItem("");
                Nilai_Ekspor.setText("");
                Pangsa_Total.setText("");
                
                Kode_Komoditas.requestFocus();
                TabelKomoditas();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Data Gagal Ditambahkan");
            }
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(KOMODITAS.class.getName()).log(Level.SEVERE, null, ex);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_INSERT_KOMActionPerformed

