    public void TabelPerikanan()
    {
        try {
            pst = con.prepareStatement("SELECT * FROM PERIKANAN");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData RSM = rs.getMetaData();
            int c;
            c = RSM.getColumnCount();
            
            MR = (DefaultTableModel)TabelPerikanan.getModel();
            MR.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++)
                {
                    v2.add(rs.getString("Kode_SDA")); 
                    v2.add(rs.getString("Provinsi"));
                    v2.add(rs.getString("Tahun"));
                    v2.add(rs.getString("Jenis_Budidaya"));
                    v2.add(rs.getString("Luas_Area_Budidaya (Ha)"));
                    v2.add(rs.getString("Jumlah_Potensi (Ton)"));
                }
                MR.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(PERIKANAN.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
