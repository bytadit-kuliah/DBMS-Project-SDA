    public void TabelProvinsi()
    {
        try {
            pst = con.prepareStatement("SELECT * FROM PROVINSI");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData RSM = rs.getMetaData();
            int c;
            c = RSM.getColumnCount();
            
            PR = (DefaultTableModel)TabelProvinsi.getModel();
            PR.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++)
                {
                    v2.add(rs.getString("Kode_Provinsi"));
                    v2.add(rs.getString("Nama_Provinsi"));
                    v2.add(rs.getString("Luas_Wilayah"));
                }
                PR.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(OPERATOR.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
