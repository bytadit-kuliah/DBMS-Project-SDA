    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed

        String Kode_Prov = KdProvinsi.getText();
        String Nama_Prov = NmProvinsi.getText();
        String Luas_Wilayah = LsWilayah.getText();

        try {
            pst = con.prepareStatement("INSERT INTO PROVINSI VALUES (?,?,?)");
            pst.setString(1, Kode_Prov);
            pst.setString(2, Nama_Prov);
            pst.setString(3, Luas_Wilayah);

            int k = pst.executeUpdate();

            if (k==1)
            {
                JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan");
                KdProvinsi.setText("");
                NmProvinsi.setText("");
                LsWilayah.setText("");
                KdProvinsi.requestFocus();
                TabelProvinsi();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Data Gagal Ditambahkan");
            }
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(OPERATOR.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_InsertActionPerformed

